#include "libft.h"

int	ft_atoi(const char *str)
{
	int					i;
	int					n;
	unsigned long long	c;

	c = 0;
	n = 1;
	i = 0;
	while (str[i] == ' ' || str[i] == '\t' || str[i] == '\v' || \
		str[i] == '\r' || str[i] == '\n' || str[i] == '\f')
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			n *= -1;
		else if (str[i] == '+')
			n *= 1;
		i++;
	}
	while (str[i] >= 48 && str[i] <= 57)
	{
		c *= 10;
		c += (str[i] - '0');
		i++;
	}	
	return ((int)(n * c));
}	
