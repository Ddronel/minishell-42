#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char		c;
	long int	nb;

	nb = n;
	if (nb < 0)
	{
		write(fd, "-", 1);
		nb *= -1;
	}
	while (nb / 10)
	{
		ft_putnbr_fd((nb / 10), fd);
		nb %= 10;
	}
	c = nb + '0';
	write (fd, &c, 1);
}
