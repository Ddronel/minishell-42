#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	*str;
	char	g;

	str = (char *)s;
	g = (char)c;
	while (*str)
	{
		if (*str == g)
			return (str);
		else
			str++;
	}
	if (*str == g)
		return (str);
	return (NULL);
}
