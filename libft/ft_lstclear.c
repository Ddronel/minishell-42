#include "libft.h"

void	ft_lstclear(t_list **lst)
{
	t_list	*l;
	t_list	*save;

	l = *lst;
	while (l)
	{
		save = l->next;
		ft_lstdelone(l);
		l = save;
	}
	*lst = NULL;
}
