int	ft_isprint(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		if ((str[i] >= 0 && str[i] <= 65) || (str[i] >= 91 && str[i] <= 96) || (str[i] >= 123 && str[i] <= 127))
			return (1);
		i++;
	}
	return (0);
}
